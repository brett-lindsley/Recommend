package com.example.demo;

public class Interaction {
	
	private int user;
	private int interaction;	
	
	public Interaction(int user, int interaction) {
		super();
		this.user = user;
		this.interaction = interaction;
	}
	
	public int getUser() {
		return user;
	}
	public int getInteraction() {
		return interaction;
	}	

}
