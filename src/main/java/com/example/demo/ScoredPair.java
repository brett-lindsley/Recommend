package com.example.demo;

public class ScoredPair {
	
	private int i;
	private int j;
	private double score;
	
	public ScoredPair(int i, int j, double score) {
		this.i = i;
		this.j = j;
		this.score = score;
	}

	public int getI() {
		return i;
	}

	public int getJ() {
		return j;
	}

	public double getScore() {
		return score;
	}

}
